    private void userLogin(String username, String password) {
        Connection dbconn=DBConnection.connectDB();
        if(dbconn!=null){
        try {
            PreparedStatement st=(PreparedStatement)
                    dbconn.prepareStatement("Select * from admin where username=? AND password=?");
            st.setString(1, username);
            st.setString(2, password);
            ResultSet res=st.executeQuery();
            if(res.next()){
                //display dashboard or new pagelogin 
                dispose();
                AdminDash d=new AdminDash();
                d.setTitle("DashBoard");
                d.setVisible(true);
            }else{
                JOptionPane.showMessageDialog(this,"Login Credentials are incorrect","âš  Caution ",JOptionPane.ERROR_MESSAGE);
            }
            //To change body of generated methods, choose Tools | Templates.
        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
        else{
             System.out.println("The connection is not available");
        }
    }
    

